//  ---------------------------------------------------------------------------------------------------------------  //
//  no.layout
//  ---------------------------------------------------------------------------------------------------------------  //

(function() {

//  ---------------------------------------------------------------------------------------------------------------  //

no.layout = {};

//  Приватное хранилище layout'ов.
var layouts = {};

//  ---------------------------------------------------------------------------------------------------------------  //

/**
    @param {string} id
    @param {!Object} layout
*/
no.layout.define = function(id, layout) {
    layouts[id] = layout;
};

/**
    @param {string} id
*/
no.layout.get = function(id, view_id) {
    var layout = layouts[id];

    if (view_id) {
        no.object.walk(layout, function(key, value) {
            if (key === view_id) {
                layout = value;
                return false;
            }
        });
    }

    return value;
};

//  ---------------------------------------------------------------------------------------------------------------  //

})();

